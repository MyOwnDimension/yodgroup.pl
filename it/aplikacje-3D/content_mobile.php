<center><h3 id='zastosowanie_2'>Zastosowanie aplikacji 3D</h3></center>
Aplikacje 3D znajdują zastosowanie w:<br>
- prezentacji obiektów 3D z możliwością ich eksploracji w czasie rzeczywistym (real time): chodzenie, otwieranie drzwi (okien), interakcja z wirtualnymi urządzęniami,<br>
- reklamie,<br>
- e-learningu - przekazywanie wiedzy na temat obsługi maszyn, urządzeń itp.<br>
- e-testingu,<br>






