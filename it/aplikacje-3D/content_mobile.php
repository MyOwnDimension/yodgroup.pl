<center><h3 id='co_to_2'>Czym są aplikacje 3D</h3></center>

Aplikacje 3D są programami, których główną treścią jest najczęśćiej grafika 3D i interakcje między jej elementmi.<br>
Aplikacje 3D najczęściej wykorzystują rendering w czasie rzeczywistym, symulując przemieszczanie się po wirtualnej scenie 3D.<br>




