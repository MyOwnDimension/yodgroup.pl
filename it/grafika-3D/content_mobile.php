<center><h3 id='zastosowanie_2'>Zastosowanie grafiki 3D</h3></center>
Grafika 3D znajduje zastosowanie w przypadku potrzeby odwzorowania nie tylko realnego obiektu, bądź powierzchni, w wirtualnej przestrzeni 3D.<br>
Dzięki temu Klient może dysponować cyfrową reprezentacją obiektu, dokonywać jego zmian, publikować, zarchiwizować, itp.<br>






