<center><h4 id='cele_1'>Cele VR</h4></center>
Celem wirtualnej rzeczywistości jest:<br>
- przeniesienie użytkownika w wirtualny świat i zanurzenie go jak największą ilością zmysłów w warunki tam panujące.<br>
- jak największe zaangażowanie zmysłów użytkownika w eksplorację wirtualnej rzczywistości.<br>
- zbudowanie poczucia wrażenia realności prezentowanej wirtualnej rzeczywistości.<br> 


