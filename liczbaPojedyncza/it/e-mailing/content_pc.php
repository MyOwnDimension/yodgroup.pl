<center><h4 id='co_to_1'>Czym jest e-mailing</h4></center>
E-mailing polega na masowym wysłaniu wiadomości elektronicznej do wcześniej zdefiniowanej grupy odbiorców.<br>
Wiadomość ma na celu poinformowanie grupy adresatów o produktach, usługach lub przekazanie innej informacji.<br>
Proces wysyłanie emaili odbywa sie automatycznie.<br>


